# Arithmatic Operator
print(10+5)
print(10-5)
print(10*5)
print(10/5)
print(10%5)
print(10//5)

# Relation Opertator
print(10>5)
print(10<5)
print(10<=5)
print(10>=5)
print(10!=5)
print(10==5)

#logical Operators
print(10<15 and 10<12)
print(10<15 or 10<12)
print(not 10<15 )


# Assignment Operators
# Initial Value first to work with Assignment Operators
x = 5
x += 2  # x = x+2
x -= 2  # x = x-2
x *= 2  # x = x*2
x /= 2  # x = x/2
